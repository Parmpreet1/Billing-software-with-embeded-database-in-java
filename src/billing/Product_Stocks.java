    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // TODO add your handling code here:
            Database db=new Database();
            db.connect();
            //get current stockes
            ResultSet rs=db.executeQuery("select * from product where product_id="+p_id.getText());
            rs.next();
            int unit=rs.getInt("unit");
            int new_add=Integer.parseInt(stockes.getText());
            int new_stockes=unit+new_add;
            db.executeUpdate("update product set unit="+new_stockes+" where product_id ="+p_id.getText());
            
            //print current table
            ResultSet rst=db.executeQuery("select * from product");
            product.setModel(DbUtils.resultSetToTableModel(rst));
            db.closecon();
            JOptionPane.showMessageDialog(this,"Stocks Upgrage Successful");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,"Invalid product id","Alert",JOptionPane.WARNING_MESSAGE);
            Logger.getLogger(Product_Stocks.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

