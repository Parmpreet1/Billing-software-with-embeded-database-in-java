    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            Database db=new Database();
            Connection con=(Connection) db.connect();
            //add new product          
            PreparedStatement pst=con.prepareStatement("insert into product values(?,?,?,?)");
            pst.setString(1, newp_id.getText());
            pst.setString(2, name.getText());
            pst.setString(3, price.getText());
            pst.setString(4, unit.getText());
            pst.executeUpdate();
            //print current table
            ResultSet rst=db.executeQuery("select * from product");
            product.setModel(DbUtils.resultSetToTableModel(rst));
            db.closecon();
            JOptionPane.showMessageDialog(this,"Product add Successful");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,"Please enter unique product id","Alert",JOptionPane.WARNING_MESSAGE);
            Logger.getLogger(Product_Stocks.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

