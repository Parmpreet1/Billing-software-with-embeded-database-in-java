    private void bequal_toActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bequal_toActionPerformed
        // TODO add your handling code here:
        switch (operation) {
            case '+':
                num2=Double.parseDouble(input.getText());
                ans=num1+num2;
                result.setText(Double.toString(ans));
                input.setText(null);
                num1=ans;
                break;
            case '*':
                num2=Double.parseDouble(input.getText());
                ans=num1*num2;
                result.setText(Double.toString(ans));
                input.setText(null);
                num1=ans;
                break;
            case '-':
                num2=Double.parseDouble(input.getText());
                ans=num1-num2;
                result.setText(Double.toString(ans));
                input.setText(null);
                num1=ans;
                break;
            case '/':
                num2=Double.parseDouble(input.getText());
                ans=num1/num2;
                result.setText(Double.toString(ans));
                input.setText(null);
                num1=ans;
                break;
            default:
                break;
        }
    }//GEN-LAST:event_bequal_toActionPerformed

