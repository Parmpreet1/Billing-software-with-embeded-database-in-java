    public static BufferedImage getScreenShot(Component comp){
        BufferedImage image=new BufferedImage(comp.getWidth(),comp.getHeight(),BufferedImage.TYPE_INT_RGB);
        comp.paint(image.getGraphics());
        return image;
    }
    public static void SaveScreenShot(Component comp,String img_name){
        BufferedImage image=getScreenShot(comp);
        try {
            ImageIO.write(image,"png",new File(img_name));
        } catch (IOException ex) {
            Logger.getLogger(billing_interface.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
 

