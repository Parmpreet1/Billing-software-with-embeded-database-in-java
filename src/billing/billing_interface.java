    private void save_billActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_billActionPerformed
        // clear previous bill info
        int i = 0;
        Database d=new Database();
        Connection con=d.connect();
        d.executeUpdate("delete from printable");
        try {
            //get and set bill no
            ResultSet rs=d.executeQuery("select count(bill_no) from bill_history");
            rs.next();
            i=rs.getInt(1);
            i++;
            bill_no.setText(Integer.toString(i));
            //save  bill Details
            String name=customer_name.getText();
            String phone=phone_no.getText();
            String mail=e_mail.getText();
            String total_s=after_gst.getText();
            String sql="insert into bill_history values("+i+",?,?,?,"+total_s+")";
            PreparedStatement stmt=con.prepareStatement(sql);
           
            stmt.setString(1, name);
            stmt.setString(2, phone);
            stmt.setString(3, mail);
            
            stmt.executeUpdate();
        } catch (SQLException ex) {
            System.out.println("SQL ERROR "+ex);
        }      
        d.closecon();
        
        SaveScreenShot(BillPanal,i+".png");
        JOptionPane.showMessageDialog(this,"Bill Save Successful");
    }//GEN-LAST:event_save_billActionPerformed

